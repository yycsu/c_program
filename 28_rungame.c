#include <stdio.h>

int main()
{
	int a[3][3];
	int i, j, k;
	a[0][0] = 7;
	a[1][0] = 8;
	a[2][0] = 9;
	for(i = 4; i < 6; i++)
	{
		for (j = 4; j < 7; j++)
		{
			for (k = 4; k < 7; k++)
			{
				if((i != j && j != k && i != k) && (15-a[0][0] - i != 15-a[1][0] - j) && (15-a[0][0] - i != 15-a[2][0] - k) && (15-a[1][0] - j != 15-a[2][0] - k))
				{
					a[0][1] = i;
					a[0][2] = 15 - 7 -i;
					a[1][1] = j;
					a[1][2] = 15 - 8 - j;
					a[2][1] = k;
					a[2][2] = 15 - 9 - j;
				}
			}
		}
	}
	if (a[0][2] == 1)
		printf("Z\n");
	if (a[1][2] == 1)
		printf("W\n");
	if (a[2][2] == 1)
		printf("L\n");
}